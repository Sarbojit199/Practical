package Java;

public class switcstatement {
		public static void main(String[] args) {
			int num =10;
			switch(num) {
			case 1:System.out.println("12");
			break;
			case 12:System.out.println("20");
			break;
			case 10:System.out.println("10");
			break;
			case 15:System.out.println("15");
			break;
			//default case statement
			default: System.out.println("number is not within our range");
			}
		}
}
